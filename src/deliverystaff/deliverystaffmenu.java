    private void selectOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectOrderActionPerformed
        try {
            for (Delivery d : DataIO.allDelivery) {
                if(d.getStaffid().getId().equals(OODMSystem.onlineStaff.getId()) && (!d.getNowstatus().equals("Pending") && !d.getNowstatus().equals("Delivery Success"))) {
                    throw new Exception();
                }  
            }
            systemExit();
            setVisible(false);
            selectOrderforDelivery Info = new selectOrderforDelivery();
            Info.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Delivery has been selected\nProceed to Update Status!");
        }
    }//GEN-LAST:event_selectOrderActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Button to update status
        Delivery a = null;
        for(Delivery d : DataIO.allDelivery) {
            if(OODMSystem.onlineStaff.getId().equals(d.getStaffid().getId()) && !d.getStatus() && (d.getNowstatus().equals("Preparing") || d.getNowstatus().equals("Out For Delivery"))) {
                a = d;
                break;
            }
        }
        if (a==null) {
            JOptionPane.showMessageDialog(null,"No Delivery Found!");
            return;
        }
        
        systemExit();
        setVisible(false);
        UpdateStatus Info = new UpdateStatus();
        Info.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

